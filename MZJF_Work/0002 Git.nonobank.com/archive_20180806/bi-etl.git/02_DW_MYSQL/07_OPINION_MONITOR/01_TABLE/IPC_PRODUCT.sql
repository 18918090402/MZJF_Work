  `THE_INVEST_TERM_UPPER` double DEFAULT NULL,
  `BANLANCE` varchar(50) DEFAULT NULL COMMENT '剩余金额',
  `OTHER_INFO` varchar(50) DEFAULT NULL COMMENT '其他信息',
  `INSERT_TIME` datetime NOT NULL COMMENT '插入时间',
