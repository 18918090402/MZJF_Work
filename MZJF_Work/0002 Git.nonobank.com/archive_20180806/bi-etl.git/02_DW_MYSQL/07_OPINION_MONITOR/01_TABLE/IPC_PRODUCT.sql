CREATE TABLE `IPC_PRODUCT` (
  `ID` int(10) NOT NULL AUTO_INCREMENT COMMENT '自增id',
  `RUN_ID` varchar(20) DEFAULT NULL COMMENT '任务ID',
  `PLATFORM_NAME` varchar(50) DEFAULT NULL COMMENT '平台名称',
  `PLATFORM_URL` varchar(50) DEFAULT NULL COMMENT '平台URL',
  `CATEGORY_NAME` varchar(50) DEFAULT NULL COMMENT '品类名称',
  `CATEGORY_URL` varchar(255) DEFAULT NULL COMMENT '品类URL',
  `PRODUCT_NAME` varchar(50) DEFAULT NULL COMMENT '产品名称',
  `PRODUCT_URL` varchar(255) DEFAULT NULL COMMENT '产品URL',
  `PRODUCT_INFO` varchar(50) DEFAULT NULL COMMENT '产品信息',
  `PROFIT` varchar(50) DEFAULT NULL COMMENT '收益率',
  `PROFIT_LOWER` double DEFAULT NULL,
  `PROFIT_UPPER` double DEFAULT NULL,
  `PROFIT_INFO` varchar(50) DEFAULT NULL COMMENT '收利率相关信息',
  `THE_INVEST_AMOUNT` int(50) DEFAULT NULL COMMENT '投资金额',
