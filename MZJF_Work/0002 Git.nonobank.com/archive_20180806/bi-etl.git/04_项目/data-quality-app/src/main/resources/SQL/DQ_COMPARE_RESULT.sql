CREATE TABLE `DQ_COMPARE_RESULT` (
  `ID` int(11) unsigned NOT NULL AUTO_INCREMENT,
  `STAT_DATE` varchar(10) NOT NULL COMMENT '日期 YYYY-MM-DD',
  `TABLE_ID` int(11) NOT NULL,
  `COLUMN_ID` int(11) NOT NULL,
  `COLUMN_NAME` varchar(64) NOT NULL DEFAULT '',
  `COLUMN_VALUE` varchar(64) DEFAULT '' COMMENT '字段实际值',
  `TARGET_VALUE` varchar(64) DEFAULT NULL COMMENT '目标值',
  `COMPARE_RESULT` int(11) NOT NULL DEFAULT '0' COMMENT '校验结果：0未校验 1成功 2失败',
  `WARNING_STATUS` int(11) DEFAULT '1' COMMENT '报警状态：1 未报警 2 已报警 3 报警失败',
  `WARNING_ENABLE` int(11) DEFAULT '0' COMMENT '冗余DQ_COLUMN_COMPARE字段',
  `WARNING_TYPE` int(11) DEFAULT NULL COMMENT '冗余DQ_COLUMN_COMPARE字段',
  `WARNING_SUCCESS_TIME` datetime DEFAULT NULL COMMENT '报警处理成功时间',
  `CREATE_TIME` datetime DEFAULT CURRENT_TIMESTAMP COMMENT '创建实际',
  `UPDATE_TIME` datetime DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '创建实际',
  PRIMARY KEY (`ID`),
  UNIQUE KEY `idx_uk` (`STAT_DATE`,`COLUMN_ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;